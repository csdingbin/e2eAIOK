def decode_cand_tuple(cand_tuple):
    cand_tuple = ast.literal_eval(cand_tuple)
    #cand_tuple = cand_tuple[1:-1].split(",")
    depth = int(cand_tuple[0])
    mlp_ratio = [float(x) for x in (cand_tuple[1:depth+1])]
    num_heads = [int(x) for x in (cand_tuple[depth + 1: 2 * depth + 1])]
    embed_dim = int(cand_tuple[-1])
    return depth, mlp_ratio, num_heads, embed_dim

